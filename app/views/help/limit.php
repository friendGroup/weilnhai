                          4、各类易腐蚀性物品。如火硫酸、盐酸、硝酸、有机溶剂、农药、双氧水、危险化学品等。 <br />
                          5、各类放射性元素及容器。如铀、钴、镭、钚等。各类烈性毒药。如铊、氰化物、砒霜等。 <br />
                          6、各类麻醉药物。如鸦片（包括罂粟壳、花、苞、叶）、吗啡、可卡因、海洛因、大麻、冰毒、麻黄素及其它制品等。 <br />
